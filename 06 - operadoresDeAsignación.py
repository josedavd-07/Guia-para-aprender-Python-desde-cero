#operadores de asignación

#Forma tradicional larga
miVariable = 5
print(miVariable)

#reasignando valñor a la variable

miVariable =  miVariable + 1
print(miVariable)

#Forma corta incrementio con reasignacion
miVariable += 1
print(miVariable)

#ahora hagamos con la resta
miVariable -= 1 #miVariable = miVariable - 1
print(miVariable)

#ahora hagamos con la multiplicacion
miVariable *= 2 #miVariable = miVariable * 2
print(miVariable)

#ahora hagamos con la division
miVariable /= 2 #miVariable = miVariable / 2
print(miVariable)
