# operadpores de comparacion de dos valores.
# nos permite comparar dos valores y nos regresa un valor booleano
a =  4
b = 2

resultado = a == b
print(f" El resultado de la comparacion a == b es: {resultado}")

#resultado si es igual o diferente del otro
resultado = a != b
print(f" El resultado de la comparacion a != b es: {resultado}")

#resultado si es mayor o igual del otro
resultado = a >= b
print(f" El resultado de la comparacion a >= b es: {resultado}")

#resultado si es menor o igual del otro
resultado = a <= b
print(f" El resultado de la comparacion a <= b es: {resultado}")

#resultado si es mayor del otro
resultado = a > b
print(f" El resultado de la comparacion a > b es: {resultado}")

#resultado si es menor del otro
resultado = a < b
print(f" El resultado de la comparacion a < b es: {resultado}")


