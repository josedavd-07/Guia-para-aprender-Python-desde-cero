#Veremos los operadores logicos: and or not
#and : Devuelve True si ambos operandos son True. a and b
#or : Devuelve True si al menos uno de los operandos es True. a or b
#not : Devuelve True si el operando es False. not a

#Declaracion de miembros variables
#Operador and
a = True
b = True

resultado = a and b
print(resultado)

#Operador or
resultado = a or b
print(resultado)

#Operador not
resultado = not a
print(resultado)